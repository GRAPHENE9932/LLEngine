TEST(GLTFLoading, TwoCubes) {
    const std::string file_name = create_temporary_file(std::span(TWO_CUBES_GLTF_DATA), "two_cubes.glb");

    llengine::GLTF gltf(file_name);

    EXPECT_EQ(gltf.textures.size(), 1);
    EXPECT_EQ(gltf.materials.size(), 1);
    EXPECT_EQ(gltf.meshes.size(), 1);
    EXPECT_EQ(gltf.nodes.size(), 1);

    EXPECT_EQ(gltf.nodes[0].transform.translation, glm::vec3(0.0f, 0.0f, 0.0f));
    EXPECT_EQ(gltf.nodes[0].transform.scale, glm::vec3(1.0f, 1.0f, 1.0f));
    expect_near_quat(gltf.nodes[0].transform.rotation, glm::quat(glm::vec3(glm::radians(45.0f), glm::radians(-45.0f), 0.0f)), 0.01f);

    EXPECT_EQ(gltf.nodes[0].children.size(), 1);
    expect_near_vec3(gltf.nodes[0].children[0].transform.translation, glm::vec3(-2.8284f, -1.0f, -1.0f), 0.01f);
    EXPECT_EQ(gltf.nodes[0].children[0].transform.scale, glm::vec3(0.5f, 0.5f, 0.5f));
    expect_near_quat(gltf.nodes[0].children[0].transform.rotation, glm::quat(glm::vec3(0.0f, 0.0f, 0.0f)), 0.01f);

    EXPECT_TRUE(compare_gltf_mesh_triangles(gltf.meshes[0], CUBE_VERTICES));

    std::filesystem::remove(file_name);
}